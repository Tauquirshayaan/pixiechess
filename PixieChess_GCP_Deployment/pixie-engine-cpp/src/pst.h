// Map piece types to their tables
// For Pixies, we can approximate by mapping them to their base type
inline const int* get_pst(int piece_type, bool is_endgame) {
    if (piece_type == KING) return is_endgame ? PST_KING_EG : PST_KING_MG;
    
    // ROCKETMAN (King type)
    if (piece_type == ROCKETMAN) return is_endgame ? PST_KING_EG : PST_KING_MG;
    
    if (piece_type == QUEEN || piece_type == FISSION_REACTOR) return PST_QUEEN;
    if (piece_type == ROOK || piece_type == PHASE_ROOK || piece_type == SUMOROOK) return PST_ROOK;
    
    // Bishops and Bishop-like Pixies
    if (piece_type == BISHOP || (piece_type >= ARISTOCRAT && piece_type <= MARAUDER)) return PST_BISHOP;
    
    // Knights and Knight-like Pixies
    if (piece_type == KNIGHT || (piece_type >= ELECTROKNIGHT && piece_type <= FISH_KNIGHT)) return PST_KNIGHT;
    
    // Pawns and Pawn-like Pixies
    if (piece_type == PAWN || (piece_type >= GOLDEN_PAWN && piece_type <= WAR_AUTOMATON)) return PST_PAWN;
    
    return nullptr;
}

